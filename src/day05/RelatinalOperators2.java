package day05;

public class RelatinalOperators2 {

    public static void main(String[] args) {

        //==;

        System.out.println(1000 == 2000);  // true
        System.out.println("Java" == "Java");  // true
        System.out.println("Java" == "java"); // false
        System.out.println("milad" == "good gay");
        System.out.println("---------------------------------------------------------");


        //!=;

        System.out.println(1000 != 2000);// true
        System.out.println("Java" != "Java"); //false
        System.out.println("Java" != "java");  // true
        System.out.println("milad" != "good gay"); //true










    }


}
