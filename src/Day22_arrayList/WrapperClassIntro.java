package Day22_arrayList;

public class WrapperClassIntro {

    public static void main(String[] args) {

        int b1 = 100;
        Integer b2 = b1; // auto boxing

        char ch = 'A';
        Character ch2 = ch; // auto boxing

        double d1 = 5.5;
        Double d2 = d1; // auto boxing

        System.out.println("-----------------------------------------------");

        Integer n1 = 20;

        int n2 = n1; // unboxing



    }
}
